// Отримуємо рік від користувача
let year = prompt("Введіть рік:");

// Перевіряємо, чи введене значення є числом
if (!isNaN(year)) {
    year = Number(year); // Перетворюємо введене значення у число

    // Перевіряємо, чи введене число є додатнім
    if (year > 0) {
        // Виводимо правильне слово в залежності від числа
        if (year === 1 || year % 10 === 1 && year % 100 !== 11) {
            console.log(year + " рік");
        } else if ((year > 1 && year < 5) || (year % 10 > 1 && year % 10 < 5 && (year % 100 < 10 || year % 100 > 20))) {
            console.log(year + " роки");
        } else {
            console.log(year + " років");
        }
    } else {
        console.log("Введене число має бути додатнім.");
    }
} else {
    console.log("Введене значення не є числом.");
}
