
let sum = 0; // Ініціалізуємо змінну для зберігання суми чисел

// Проходимося від 1 до 100 включно
for (let i = 1; i <= 100; i++) {
    // Перевіряємо, чи поточне число не кратне 3
    if (i % 3 !== 0) {
        // Додаємо поточне число до загальної суми
        sum += i;
    }
}

// Виводимо суму на екран
console.log("Сума чисел від 1 до 100, які не кратні 3: " + sum);
