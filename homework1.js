
// Регулярний вираз, який допоможе знайти принаймні шість символів і не містити літери А (великої або малої)
const regex = /^[^Aa]{6,}$/;

//^ - початок рядка
//[^Aa] - будь-який символ, який не є літерою "A" (велика або маленька)
//{6,} - щонайменше шість символів підряд
//$ - кінець рядка

// Перевірка рядків
const strings = ["abcdef", "123456", "aBcDeF", "AbCdEfG", "aaaBBB", "12345"];

strings.forEach(str => {
    if (regex.test(str)) {
        console.log(`Рядок "${str}" відповідає умовам.`);
    } else {
        console.log(`Рядок "${str}" не відповідає умовам.`);
    }
});
